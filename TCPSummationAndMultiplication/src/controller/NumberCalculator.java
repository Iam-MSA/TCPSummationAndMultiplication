package controller;


/**
 * This is a controller class that manage the arithmetic operations.
 * 
 * @author Mirza Sahid Afridi
 *
 */

public class NumberCalculator {
	/**
	 * This method will return the sum of three numbers
	 * @param number1, number2, number3
	 * 
	 * @return the result of sum
	 */
	public int getSum(int number1, int number2, int number3)
	{
		return number1 + number2 + number3;
	}
	
	
	/**
	 * This method will return the product for multiplication of three numbers
	 * @param number1, number2, number3
	 * 
	 * @return the result of multiplication
	 */
	public int getMultiplication(int number1, int number2, int number3)
	{
		return number1 * number2 * number3;
	}
	
	
	
}
